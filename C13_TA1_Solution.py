# Define a function with 2 inputs - first_name, second_name. Name it as "full_name". 
# Function must concatenate the two inputs seperated by a space.

def full_name (first_name, second_name):
  name = first_name + ' ' + second_name
  return name
  
# Call the "full_name" function by passing inputs and print the result function returns 
print(full_name("Roger", "Federer")) 